// Helper function to get genre by ID
export const getGenreById = (genreId) => {
  return GENRES.find(g => g.id === genreId);
};

// Helper function to get all genre names sorted alphabetically
export const getSortedGenres = () => {
  return [...GENRES].sort((a, b) => a.name.localeCompare(b.name));
};
